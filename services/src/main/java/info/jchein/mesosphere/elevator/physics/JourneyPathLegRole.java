package info.jchein.mesosphere.elevator.physics;

import java.util.EnumMap;

public enum JourneyPathLegRole {
	FORWARD_JERK_ONE,
	FORWARD_ACCELERATION,
	REVERSE_JERK_ONE,
	CONSTANT_VELOCITY,
	REVERSE_JERK_TWO,
	REVERSE_ACCELERATION,
	FORWARD_JERK_TWO,
	TERMINAL_SEGMENT;
	
	private static final EnumMap<JourneyPathLegRole, JourneyPathLegRole> ARC_PATH_ORDER =
		new EnumMap<JourneyPathLegRole, JourneyPathLegRole>(JourneyPathLegRole.class);
	
	static {
		ARC_PATH_ORDER.put(FORWARD_JERK_ONE, FORWARD_ACCELERATION);
		ARC_PATH_ORDER.put(FORWARD_ACCELERATION, REVERSE_JERK_ONE);
		ARC_PATH_ORDER.put(REVERSE_JERK_ONE, CONSTANT_VELOCITY);
		ARC_PATH_ORDER.put(CONSTANT_VELOCITY, REVERSE_JERK_TWO);
		ARC_PATH_ORDER.put(REVERSE_JERK_TWO, REVERSE_ACCELERATION);
		ARC_PATH_ORDER.put(REVERSE_ACCELERATION, FORWARD_JERK_TWO);
		ARC_PATH_ORDER.put(FORWARD_JERK_TWO, TERMINAL_SEGMENT);
	}
	
	JourneyPathLegRole getNextLeg() {
		return ARC_PATH_ORDER.get(this);
	}
}
